"""Contract / agreement template skeleton.

Covers common Sri Lankan contract structures: service agreements,
lease agreements, employment contracts, and general commercial contracts.
"""

CONTRACT_TEMPLATE = """\
## CONTRACT STRUCTURE

Draft a legally sound contract with the following sections.  Fill each
section using the retrieved legal context and the user's request.
Cite the statutory authority for key clauses using [LAW-*] anchors.

### 1. TITLE AND PARTIES
- Full title of the agreement
- Names, addresses, and designations of all parties
- Date of execution

### 2. RECITALS / PREAMBLE
- Background and purpose of the agreement
- "WHEREAS" clauses establishing the factual basis

### 3. DEFINITIONS
- Define key terms used throughout the contract

### 4. OPERATIVE CLAUSES
- Core obligations and rights of each party
- Consideration / payment terms
- Performance timelines and milestones

### 5. REPRESENTATIONS AND WARRANTIES
- Key representations made by each party

### 6. TERMINATION
- Grounds for termination (breach, convenience, force majeure)
- Notice period requirements
- Consequences of termination

### 7. DISPUTE RESOLUTION
- Governing law (Laws of Sri Lanka)
- Jurisdiction (relevant Sri Lankan court)
- Arbitration / mediation provisions if applicable

### 8. GENERAL PROVISIONS
- Entire agreement clause
- Amendment procedures
- Severability
- Notices

### 9. EXECUTION
- Signature blocks for all parties
- Witness attestation blocks
"""
